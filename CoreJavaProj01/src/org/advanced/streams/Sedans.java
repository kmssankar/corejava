package org.advanced.streams;

public class Sedans {
	
	String brand;
	int cubicCapacity;
	String model;
	/**
	 * @param brand
	 * @param cubicCapacity
	 * @param model
	 */
	public Sedans(String brand, int cubicCapacity, String model) {
		super();
		this.brand = brand;
		this.cubicCapacity = cubicCapacity;
		this.model = model;
	}
}
